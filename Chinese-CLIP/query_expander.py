def expand_query(query: str, max_synonyms: int = 2) -> List[str]:
    """
    返回 [原始 query] + 同义词扩展列表（最多 max_synonyms 个扩展词）。

    匹配策略：
      1. 精确匹配：query 本身在词典中
      2. 包含匹配：query 包含词典中的某个关键词，则将该关键词替换为同义词
    """
    queries: List[str] = [query]

    # 1. 精确匹配
    if query in SYNONYM_DICT:
        queries += SYNONYM_DICT[query][:max_synonyms]
        return queries

    # 2. 包含匹配：按关键词从长到短排序，优先匹配更具体的词
    sorted_keys = sorted(SYNONYM_DICT.keys(), key=len, reverse=True)
    for key in sorted_keys:
        if key in query:
            for syn in SYNONYM_DICT[key][:max_synonyms]:
                expanded = query.replace(key, syn, 1)
                if expanded not in queries:
                    queries.append(expanded)
            break  # 只替换第一个匹配

    return queries


def reciprocal_rank_fusion(
    ranked_lists: List[List[Tuple[str, float]]],
    k: int = 60,
) -> List[Tuple[str, float]]:
    """
    倒数排名融合（Reciprocal Rank Fusion，RRF）。

    Args:
        ranked_lists: 每个子列表是 [(product_code, score), ...]，按 score 降序排列。
        k:            RRF 惩罚参数，默认 60。
    Returns:
        融合后的 [(product_code, rrf_score), ...]，按 rrf_score 降序。
    """
    rrf_scores: Dict[str, float] = {}
    for ranked in ranked_lists:
        for rank, (code, _score) in enumerate(ranked):
            rrf_scores[code] = rrf_scores.get(code, 0.0) + 1.0 / (k + rank + 1)

    return sorted(rrf_scores.items(), key=lambda x: x[1], reverse=True)
